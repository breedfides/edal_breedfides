package de.ipk_gatersleben.bit.bi.edal.primary_data.file.implementation;

import java.io.Serializable;
import java.security.Principal;

/**
 * Class to represent all internal stored principals.
 * 
 * @author arendd
 */
public class ALLPrincipal implements Principal, Serializable {

	private static final long serialVersionUID = 1L;

	private String name = "ALL";

	public ALLPrincipal() {
	}

	/**
	 * do nothing
	 * 
	 * @param name
	 *            standard name
	 */
	public ALLPrincipal(String name) {
	}

	@Override
	public String getName() {
		return name;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (!(obj instanceof ALLPrincipal)) {
			return false;
		}
		ALLPrincipal other = (ALLPrincipal) obj;
		if (name == null) {
			if (other.name != null) {
				return false;
			}
		} else if (!name.equals(other.name)) {
			return false;
		}
		return true;
	}

	@Override
	public String toString() {
		return "ALLPrincipal: " + name;
	}
}